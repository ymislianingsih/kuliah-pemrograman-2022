

public class Bengkel1 {

    private String NamaPelanggan;
    private String JenisService;
    private String Mekanik;
    private String TanggalMasuk;
    private String TanggalKeluar;
    private String Keterangan;


    public String getNamaPelanggan() {
        return NamaPelanggan;
    }

    public void setNamaPelanggan(String namaPelanggan) {
        NamaPelanggan = namaPelanggan;
    }

    public String getJenisService() {
        return JenisService;
    }

    public void setJenisService(String jenisService) {
        JenisService = jenisService;
    }

    public String getMekanik() {
        return Mekanik;
    }

    public void setMekanik(String mekanik) {
        Mekanik = mekanik;
    }

    public String getTanggalMasuk() {
        return TanggalMasuk;
    }

    public void setTanggalMasuk(String tanggalMasuk) {
        TanggalMasuk = tanggalMasuk;
    }

    public String getTanggalKeluar() {
        return TanggalKeluar;
    }

    public void setTanggalKeluar(String tanggalKeluar) {
        TanggalKeluar = tanggalKeluar;
    }

    public String getKeterangan() {
        return Keterangan;
    }

    public void setKeterangan(String keterangan) {
        Keterangan = keterangan;
    }


}